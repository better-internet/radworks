  <h2>How Personal Data is Used</h2>

  <p>
    If You go to the Radworks Websites, the Foundation does not collect any of Your information and
    data as You browse. However, the Radworks Websites are hosted by Netlify, Inc., 44 Montgomery
    Street, Suite 300, San Francisco, CA 94104 (“Netlify”), which does collect Your data (e.g., IP
    address, user agent, referrer, full URL with query string) and which uses cookies and log files
    to track user information. They use cookies to track which page variant a visitor/User has seen,
    to track if a visitor/User has clicked on a page variant, to monitor traffic patterns and to
    gauge popularity of service options. This information is used to deliver relevant content and
    services to You and other users. Generally, this data is stored by Netlify for 30 days.
    Furthermore, the Foundation has enabled <a
      href="https://plausible.io/docs/outbound-link-click-tracking">Plausible Analytics</a
    >, to better understand the Radworks User community based on the User information Plausible
    collects about Websites visitors. Plausible Analytics does collect minimal amounts of Your data
    (e.g. page URL, HTTP referer, browser, operating system, device type, and country/region/city)
    but all site measurement is carried out anonymously and does not use cookies. This data is
    collected and processed by Plausible and in fully anonymized data form only. For more
    information about the information collected, visit Plausible’s data policies
    <a href="https://plausible.io/data-policy">here</a>.
  </p>

